        }

        public static void Update(GameTime gameTime)
        {
            Input.GetInput();

            if (Library.PlayerInstance != null)
            {
                if (Library.fieldPathfinding.target != null)
                {
                    if (!Library.PlayerInstance.hitbox.Intersects(Library.fieldPathfinding.target.hitbox))
                    {
                        Library.fieldPathfinding.UpdateTarget();

                        Library.fieldPathfinding.CreatePathfindingField();
                    }
                }
                else
                {
                    Library.fieldPathfinding.UpdateTarget();

                    Library.fieldPathfinding.CreatePathfindingField();
                }
            }

            for (int i = 0; i < Library.gameObjects.Count; i++)
            {
                Library.gameObjects[i].Update(gameTime);
            }

            for (int i = Library.gameObjects.Count - 1; i >= 0; i--)
            {
                if (Library.gameObjects[i].IsRemoved)
                {
                    Library.gameObjects.RemoveAt(i);
                }
            }

            #region Debug

            if (Input.MouseIsPressed(Input.currentMS.LeftButton))
            {
                for (int i = 0; i < 10; i++)
                {
                    Library.gameObjects.Add(new FastEnemy(Input.currentMS.Position.ToVector2()));
                }
            }

            //if (Input.MouseHasBeenPressed(Input.currentMS.LeftButton, Input.prevMS.LeftButton))
            //{
            //    Library.gameObjects.Add(new FastEnemy(Input.currentMS.Position.ToVector2()));
            //}

            if (Input.MouseHasBeenPressed(Input.currentMS.RightButton, Input.prevMS.RightButton))
            {
                for (int i = 0; i < Library.gameObjects.Count; i++)
                {
                    if (Library.gameObjects[i] is Enemy e)
                    {
                        if (Input.GetMouseBounds(true).Intersects(e.hitbox))
                        {
                            e.Destroy();
                        }
                    }
                }
            }
            if (Input.HasBeenPressed(Keys.Z))
            {
                if (Library.showDistance)
                {
                    Library.showDistance = false;
                }
                if (Library.showArrows2)
                {
                    Library.showArrows2 = false;
                }

                Library.showArrows1 = !Library.showArrows1;
            }

            if (Input.HasBeenPressed(Keys.X))
            {
                if (Library.showDistance)
                {
                    Library.showDistance = false;
                }
                if (Library.showArrows1)
                {
                    Library.showArrows1 = false;
                }

                Library.showArrows2 = !Library.showArrows2;
            }

            if (Input.HasBeenPressed(Keys.C))
            {
                if (Library.showArrows1)
                {
                    Library.showArrows1 = false;
                }
                if (Library.showArrows2)
                {
                    Library.showArrows2 = false;
                }

                Library.showDistance = !Library.showDistance;
            }
            #endregion
        }

        public static void Draw(SpriteBatch spriteBatch)
        {
            Library.TileMap.Draw(spriteBatch);

            for (int i = 0; i < Library.gameObjects.Count; i++)
            {
                Library.gameObjects[i].Draw(spriteBatch);
            }
        }
