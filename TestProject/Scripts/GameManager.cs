        public void LoadContent(ContentManager content, GraphicsDevice graphics)
        {
            textureManager = new TextureManager();

            textureManager.LoadTextures(content, graphics);
        }

        public void Update(GameTime gameTime)
        {
            Input.GetInput();

            Library.fieldPathfinding.SetTarget();

            if (Input.MouseHasBeenPressed(Input.currentMS.LeftButton, Input.prevMS.LeftButton))
            {
                Library.gameObjects.Add(new Enemy(Input.currentMS.Position.ToVector2()));
            }

            for (int i = 0; i < Library.gameObjects.Count; i++)
            {
                Library.gameObjects[i].Update(gameTime);
            }

            for (int i = Library.gameObjects.Count - 1; i >= 0; i--)
            {
                if (Library.gameObjects[i].IsRemoved)
                {
                    Library.gameObjects.RemoveAt(i);
                }
            }
        }

        public void Draw(SpriteBatch spriteBatch)
        {
